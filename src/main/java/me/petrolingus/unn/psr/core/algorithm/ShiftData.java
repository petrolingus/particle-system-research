package me.petrolingus.unn.psr.core.algorithm;

public class ShiftData {

    double t;
    double value;

    public ShiftData(double t, double value) {
        this.t = t;
        this.value = value;
    }

    public double getT() {
        return t;
    }

    public double getValue() {
        return value;
    }
}
